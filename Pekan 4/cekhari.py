nilai = int(input("Masukkan hari:"))

if nilai == 1:
	print("Senin")
elif nilai == 2:
	print("Selasa")
elif nilai == 3:
	print("Rabu")
elif nilai == 4:
	print("Kamis")
elif nilai == 5:
	print("Jumat")
elif nilai == 6:
	print("Sabtu")
elif nilai == 7:
	print("Ahad")
else:
    print("Angka yang anda masukkan salah")